package com.cogent.loop;

public class loopConcepts {
	
	void runLoops(int initial_value,int find_value) {
		int counter =1;
		for (int i = initial_value; i <= find_value; i++) {
			
			System.out.println(i);
			System.out.println(counter);
//			System.out.println();
			
			counter++;
		}

		
		
	}
	

}

